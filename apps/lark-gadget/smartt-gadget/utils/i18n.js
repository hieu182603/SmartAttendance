function getTranslations() {
  let lang = "vi";
  try {
    lang = tt.getStorageSync("settings_language") || "vi";
  } catch (e) {}
  return lang === "en" ? en : vi;
}

function getLanguage() {
  try {
    return tt.getStorageSync("settings_language") || "vi";
  } catch (e) {
    return "vi";
  }
}

function setLanguage(lang) {
  try {
    tt.setStorageSync("settings_language", lang);
  } catch (e) {}
}

function getTheme() {
  try {
    const isDarkMode = tt.getStorageSync("settings_darkMode");
    if (isDarkMode !== null && isDarkMode !== undefined && isDarkMode !== "") {
      return JSON.parse(isDarkMode) ? "dark" : "light";
    }
    const theme = tt.getStorageSync("settings_theme");
    if (theme) return theme;

    // Fallback to system theme
    const sysInfo = tt.getSystemInfoSync();
    if (sysInfo && sysInfo.theme) {
      return sysInfo.theme;
    }
    return "light";
  } catch (e) {
    return "light";
  }
}

function setTheme(theme) {
  try {
    tt.setStorageSync("settings_theme", theme);
    tt.setStorageSync("settings_darkMode", JSON.stringify(theme === "dark"));
  } catch (e) {}
}


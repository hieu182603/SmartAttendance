export const hasPermissionFromList = (permissions, permission) => {
  const permissionList = Array.isArray(permissions) ? permissions : [];
  if (permissionList.includes(permission)) return true;
  const higher = PERMISSION_HIERARCHY[permission] || [];
  return higher.some((p) => permissionList.includes(p));
};

